#include <iostream> 
using namespace std; 

int main() 
{ 
	cout << "sizeof(1): " << sizeof(1) << "\n";
	cout << "sizeof(1L): " << sizeof(1L) << "\n";
	cout << "sizeof(1LL): " << sizeof(1LL) << "\n";
	cout << "sizeof(1.0): " << sizeof(1.0) << "\n";
	cout << 26 << "\n"; // dec
	cout << 032 << "\n"; // oct
	cout << 0x1A << "\n"; // hex

    return 0; 
}

